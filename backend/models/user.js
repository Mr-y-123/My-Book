const mongoose = require("mongoose");

const userScheme = new mongoose.Schema({
  name: {
    type: String,
    require: true,
  },
  email: {
    type: String,
    require: true,
  },
  password: {
    type: String,
    require: true,
  },
  date: {
    type: Date,
    default: Date.now,
  },
});

const user = mongoose.model("users", userScheme);

module.exports = user;
